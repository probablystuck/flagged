interface Props {
  handleFlagInput: (input: string, output: string) => void
}

const InputBox = ({ handleFlagInput }: Props) => {
  const styles = useStyles()
  const reference = useRef<HTMLTextAreaElement>()

  function onFlagInput() {
    if (reference.current !== undefined) {
      const output = flagMap.get(reference.current.value)

      if (output !== undefined) {
        handleFlagInput(reference.current.value, output)
        reference.current.value = ""
      }
    }
  }

  return (
    <div className={styles.root}>
      <TextField
        id="outlined-basic"
        label="Input Flags Here"
        variant="outlined"
        inputRef={reference}
      />
      <Button
        variant="contained"
        className={styles.button}
        onClick={onFlagInput}
      >
        Translate Flags
      </Button>
    </div>
  )
}

export default InputBox
